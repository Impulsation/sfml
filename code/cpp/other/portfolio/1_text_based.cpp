#include <iostream>

using namespace std;

int main(){
	char input;
	cout << "Input: " << endl;
	cin >> input;

	switch (input)
	{
		case 'a':
			cout << "a" << endl;
			break;
		case 'b':
			cout << "b" << endl;
			break;
		case 'c':
			cout << "c" << endl;
			break;
		case 'd':
			cout << "d" << endl;
	}
	return 0;
}
